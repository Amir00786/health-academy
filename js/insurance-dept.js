    function updateProgress() {
      const course = currentCourse();
      const total = course.lessons.length;
      const done = progress.size;
      const pct = total ? Math.round(done / total * 100) : 0;
      document.getElementById('progFill').style.width = pct + '%';
      document.getElementById('progPct').textContent = pct + '%';
      const btn = document.getElementById('examBtn');
      if (!btn) return;
      if (done >= total) {
        btn.textContent = lang() === 'ar' ? '← تحميل الشهادة' : 'Download certificate →';
        btn.onclick = () => Certificate.generate(course.cert.name);
      } else {
        btn.textContent = lang() === 'ar' ? '← ابدأ الدورة' : 'Start course →';
        btn.onclick = () => document.querySelector('.lessons-list').scrollIntoView({ behavior: 'smooth' });
      }
    }

